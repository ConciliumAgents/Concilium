#!/usr/bin/env bash
# kb-refresh.sh - Refresh live KB files: regenerate diff.patch and capture test output
# Usage: kb-refresh.sh <repo> ["<Test command>"]
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=_lib.sh
source "${SCRIPT_DIR}/_lib.sh"

REPO="$(loop_resolve_repo "${1:-}")"
TESTCMD="${2:-$LOOP_TEST_CMD}"
TABLE="$(loop_table_dir "${REPO}")"

# --- diff.patch: current diff (tracked files vs HEAD + untracked file list) ---
{
  echo "# Current diff snapshot (generated by kb-refresh)"
  echo "## git diff (tracked files vs HEAD)"
  git -C "${REPO}" diff HEAD 2>/dev/null || git -C "${REPO}" diff 2>/dev/null || echo "(no commits or no changes)"
  echo
  echo "## Untracked files"
  git -C "${REPO}" ls-files --others --exclude-standard 2>/dev/null || true
} > "${TABLE}/KB/diff.patch"
loop_log "Refreshed KB/diff.patch"

# --- test-results.txt ---
if [ -n "${TESTCMD}" ]; then
  loop_log "Running tests: ${TESTCMD}"
  set +e
  out="$( cd "${REPO}" && eval "${TESTCMD}" 2>&1 )"; rc=$?
  set -e
  { echo "# Test command: ${TESTCMD}"; echo "---"; echo "${out}"; echo "---"; echo "# Exit code: ${rc}"; } > "${TABLE}/KB/test-results.txt"
  loop_log "test exit code=${rc}; wrote KB/test-results.txt"
else
  echo "(No LOOP_TEST_CMD configured and no test command passed; automated tests skipped this round.)" > "${TABLE}/KB/test-results.txt"
  loop_log "No test command configured; skipped"
fi
